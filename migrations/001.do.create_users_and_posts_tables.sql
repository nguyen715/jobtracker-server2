CREATE TABLE users (
  id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  email TEXT NOT NULL,
  token TEXT NOT NULL
);

CREATE TABLE posts (
  id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  title TEXT NOT NULL,
  url TEXT NOT NULL,
  location TEXT,
  notes TEXT,
  rating INTEGER DEFAULT 1,
  date_created TIMESTAMPTZ DEFAULT now(),
  user_email TEXT REFERENCES users NOT NULL,
  user_token TEXT REFERENCES users NOT NULL
);